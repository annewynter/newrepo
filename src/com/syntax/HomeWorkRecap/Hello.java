package com.syntax.HomeWorkRecap;

public class Hello {
void printHello (String country) {
	switch (country) {
	case "USA":
		System.out.println("hi");
		break;
	case "Italy":
		System.out.println("Ciao");
		break;
	case "Egypt":
		System.out.println("Salam");
	break;
	default:
		System.out.println("Invalid country name");
			
	}
}
}
